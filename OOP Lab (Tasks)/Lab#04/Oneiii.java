class Oneiii 
{
    public static void main(String[]nah)
    {
        for(int a=1;a<6;a++)
        {
            for(int b=6;b>=a;b--)
            {
                System.out.print(" ");
            }
            for(int c=1;c<=(2*a-1);c++)
            {
                System.out.print("*");
            }
            System.out.println();
        }
    }
}